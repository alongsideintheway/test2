package com.chzu.pojo;

public class Updata {
  private int wid;
  private String updatalocation;
  private String course;
  private int id;
  private String tname;
  private String profile;

    public String getProfile() {
        return profile;
    }

    public void setProfile(String profile) {
        this.profile = profile;
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public int getWid() {
        return wid;
    }

    public void setWid(int wid) {
        this.wid = wid;
    }

    public String getUpdatalocation() {
        return updatalocation;
    }

    public void setUpdatalocation(String updatalocation) {
        this.updatalocation = updatalocation;
    }
}
